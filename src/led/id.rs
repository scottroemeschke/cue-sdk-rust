//! Contains the `LedId` enum, with subcategory of device types, and all of the
//! current supported values that may be returned from the iCUE SDK.
use cue_sdk_sys as ffi;
use num_traits::FromPrimitive;

/// All of the valid `LedId`s currently in the iCUE SDK.
/// They are broken into subcategories, each with a number of available values.
#[derive(Debug, Copy, Clone, PartialEq)]
pub enum LedId {
    Keyboard(KeyboardLedId),
    Mouse(MouseLedId),
    MouseMat(MouseMatLedId),
    Headset(HeadsetLedId),
    HeadsetStand(HeadsetStandLedId),
    Memory(MemoryModuleLedId),
    Motherboard(MotherboardLedId),
    GraphicsCard(GraphicsCardLedId),
    Commander(CommanderLedId),
    Device(DeviceLedId),
    Integrated(IntegratedLedId),
    Unknown(u32),
}

impl From<LedId> for ffi::CorsairLedId {
    fn from(led_id: LedId) -> Self {
        match led_id {
            LedId::Keyboard(id) => id as u32,
            LedId::Mouse(id) => id as u32,
            LedId::MouseMat(id) => id as u32,
            LedId::Headset(id) => id as u32,
            LedId::HeadsetStand(id) => id as u32,
            LedId::Memory(id) => id as u32,
            LedId::Motherboard(id) => id as u32,
            LedId::GraphicsCard(id) => id as u32,
            LedId::Commander(id) => id as u32,
            LedId::Device(id) => id as u32,
            LedId::Integrated(id) => id as u32,
            LedId::Unknown(id) => id,
        }
    }
}

impl From<ffi::CorsairLedId> for LedId {
    fn from(led_id: ffi::CorsairLedId) -> Self {
        KeyboardLedId::from_u32(led_id)
            .map(|id| LedId::Keyboard(id))
            .or_else(|| MouseLedId::from_u32(led_id).map(|id| LedId::Mouse(id)))
            .or_else(|| MouseMatLedId::from_u32(led_id).map(|id| LedId::MouseMat(id)))
            .or_else(|| HeadsetLedId::from_u32(led_id).map(|id| LedId::Headset(id)))
            .or_else(|| HeadsetStandLedId::from_u32(led_id).map(|id| LedId::HeadsetStand(id)))
            .or_else(|| MemoryModuleLedId::from_u32(led_id).map(|id| LedId::Memory(id)))
            .or_else(|| MotherboardLedId::from_u32(led_id).map(|id| LedId::Motherboard(id)))
            .or_else(|| GraphicsCardLedId::from_u32(led_id).map(|id| LedId::GraphicsCard(id)))
            .or_else(|| CommanderLedId::from_u32(led_id).map(|id| LedId::Commander(id)))
            .or_else(|| DeviceLedId::from_u32(led_id).map(|id| LedId::Device(id)))
            .or_else(|| IntegratedLedId::from_u32(led_id).map(|id| LedId::Integrated(id)))
            .unwrap_or_else(|| LedId::Unknown(led_id))
    }
}

